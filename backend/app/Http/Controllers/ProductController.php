    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $p=new Product;
        $p->name=$request->name;
        $p->price=$request->price;
        $p->discount=$request->discount;
        $p->avg_rating=$request->avg_rating;
        $p->description=$request->description;
        $p->warrenty=$request->warrenty;
        $p->covered_in_warrenty=$request->covered_in_warrenty;
        $p->not_covered_in_warrenty=$request->not_covered_in_warrenty;
        $p->delivery_charge=$request->delivery_charge;
        $p->quantity=$request->quantity;
        $p->save();
        $msg = 'Product is added';
        return response()->json(array($msg),200);
       


    }

